#include "Window.h"

#include "Logger.h"

///////////////////////////////////////////////////////////////////////////////
// Constructor
///////////////////////////////////////////////////////////////////////////////
Window::Window(Vector2 size) {
	_size = size;
}

///////////////////////////////////////////////////////////////////////////////
// State Methods
///////////////////////////////////////////////////////////////////////////////
void Window::init() {
	if (_currState != Uninitalized) {
		CORE_ERROR("Attempted to initalize an already initalized Window");
		return;
	}
}

void Window::restart() {
	if (_currState != Running) {
		CORE_ERROR("A Window must be running to restart it");
		return;
	}
}

